    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $cars = Cars::all();
        $inProgress = Cars::where('status', 0)->get();
        return view('cars.index', compact('cars', 'inProgress'));
    }

    /**
